/**
 * Implements the OTel IdGenerator interface using the Web Crypto API.
 * Method names match opentelemetry-js IdGenerator: generateTraceId / generateSpanId.
 */

/** Generates a random 16-byte (32 hex char) trace ID. */
export function generateTraceId(): string {
  const bytes = new Uint8Array(16);
  crypto.getRandomValues(bytes);
  return Array.from(bytes, (b) => b.toString(16).padStart(2, "0")).join("");
}

/** Generates a random 8-byte (16 hex char) span ID. */
export function generateSpanId(): string {
  const bytes = new Uint8Array(8);
  crypto.getRandomValues(bytes);
  return Array.from(bytes, (b) => b.toString(16).padStart(2, "0")).join("");
}
